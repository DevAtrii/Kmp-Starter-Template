package com.kmpstarter.ui_components.forms

import androidx.compose.foundation.layout.Arrangement
import androidx.compose.foundation.layout.Column
import androidx.compose.foundation.layout.ColumnScope
import androidx.compose.foundation.layout.padding
import androidx.compose.material3.MaterialTheme
import androidx.compose.material3.Text
import androidx.compose.runtime.Composable
import androidx.compose.ui.Alignment
import androidx.compose.ui.Modifier
import androidx.compose.ui.text.font.FontWeight
import com.kmpstarter.ui_utils.theme.Dimens

/**
 * Clean form section container with consistent styling.
 */
@Composable
fun CleanForm(
    title: String,
    modifier: Modifier = Modifier,
    textModifier: Modifier = Modifier.padding(bottom = Dimens.paddingSmall),
    verticalArrangement: Arrangement.Vertical = Arrangement.Top,
    horizontalAlignment: Alignment.Horizontal = Alignment.Start,
    content: @Composable ColumnScope.() -> Unit,
) {
    Column(
        modifier = modifier,
        horizontalAlignment = horizontalAlignment,
        verticalArrangement = verticalArrangement
    ) {
        // Section header
        if (title.isNotBlank())
            Text(
                text = title,
                style = MaterialTheme.typography.titleMedium.copy(
                    fontWeight = FontWeight.SemiBold
                ),
                color = MaterialTheme.colorScheme.onSurface,
                modifier = textModifier.padding(bottom = Dimens.paddingSmall)
            )

        // Section content
        content()
    }
}